    private void bttnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnDeleteActionPerformed
        // TODO add your handling code here:
         if (!tabel_klik.equals("")){
            try
            {
                Connection conn = KoneksiDB.getKoneksi();

                String query = "DELETE FROM tabel_pegawai_5200411474 "+"WHERE id_pegawai=?";

                PreparedStatement p = conn.prepareStatement(query);

                p.setString(1, tabel_klik);

                int rowsDeleted = p.executeUpdate();
                if (rowsDeleted > 0){
                    JOptionPane.showMessageDialog(this, "Proses Delete berhasil", "Success", JOptionPane.INFORMATION_MESSAGE );
                }
                p.close();
            }
            catch (SQLException e)
            {
                JOptionPane.showMessageDialog(this, "Proses delete gagal...", "Error", JOptionPane.ERROR_MESSAGE);
            }
            finally
            {
                this.loadDataToTable();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Anda belum memilih data pegawai yang akan dihapus", "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_bttnDeleteActionPerformed

