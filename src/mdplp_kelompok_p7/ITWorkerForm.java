 public void loadDataToTable()
    {
        DefaultTableModel dm = (DefaultTableModel)jTable1.getModel();
        while(dm.getRowCount() > 0)
        {
            dm.removeRow(0);
        }
        
        try {
            Connection conn = KoneksiDB.getKoneksi();
            Statement s = conn.createStatement();
            
            String query = "SELECT * FROM tabel_pegawai_5200411474";
            ResultSet result = s.executeQuery(query);
            
            while (result.next()) {
                Object [] obj = new Object[5];
                obj[0]= result.getString("nama_pegawai");
                obj[1]= result.getString("no_pegawai");
                obj[2]= result.getString("posisi_pegawai");
                obj[3]= result.getString("gaji_pegawai");
                obj[4]= result.getInt("id_pegawai");
                
                
                model.addRow(obj);
                
            } 
            result.close();
            s.close();
        } 
        catch (SQLException e) 
        {
            System.out.println(e.getMessage());
        }
        finally
        {
            tabel_klik="";
            
            jTable1.getColumnModel().getColumn(4).setMinWidth(0);
            jTable1.getColumnModel().getColumn(4).setMaxWidth(0);
        }
    }

 
    public void insertDataPegawai() throws NoPegawaiHarusAngka, NoPegawaiHarusPositif, NoPegawaiHarus10Digit
    {
       
        long nopegawai;
        try 
        {
            nopegawai = Long.parseLong(txtNoPegawai.getText());
        } 
        catch (NumberFormatException e) 
        {
            throw new NoPegawaiHarusAngka();
        }
        
        if (nopegawai < 0)
        {
            throw new NoPegawaiHarusPositif();
        }
        if (txtNoPegawai.getText().length() < 10 )
       {
           throw new NoPegawaiHarus10Digit();
       }
        if (txtNoPegawai.getText().length() > 10 )
       {
           throw new NoPegawaiHarus10Digit();
       }
    }
